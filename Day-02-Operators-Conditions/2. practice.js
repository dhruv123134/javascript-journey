// Even or Odd
let num = 7;

if (num % 2 === 0) {
  console.log("Even");
} else {
  console.log("Odd");
}

// Positive / Negative / Zero
let number = -5;

if (number > 0) {
  console.log("Positive");
} else if (number < 0) {
  console.log("Negative");
} else {
  console.log("Zero");
}

// Greater number
let a = 10;
let b = 20;

if (a > b) {
  console.log("A is greater");
} else {
  console.log("B is greater");
}
